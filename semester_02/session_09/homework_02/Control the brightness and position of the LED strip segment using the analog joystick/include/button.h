// button.h

// Для стандартных тактовых кнопок в проектах на Arduino оптимальной считается
// частота опроса в диапазоне от 20 до 100 Гц (интервал 10–50 мс). Этот
// промежуток позволяет эффективно подавлять дребезг контактов, сохраняя при
// этом мгновенную реакцию на действия пользователя.
///////////////////////////////////////////////////////////////////////////////
// Ключевые рекомендации по частоте
// 1. Низкая (100–200 мс): Максимально допустимая задержка, которую человек еще
// не воспринимает как «торможение» интерфейса.
// 2. Стандартная (20–50 мс): Оптимально для большинства бытовых кнопок. Этого
// времени достаточно, чтобы механические
//    колебания контактов («дребезг») утихли.
// 3. Высокая (5–10 мс): Используется в динамичных приложениях (например,
// игровые контроллеры), где важна минимальная
//    задержка ввода.
///////////////////////////////////////////////////////////////////////////////
// Методы реализации
// 1. Программная задержка (delay): Самый простой способ — поставить паузу после
// считывания. Минус: Блокирует выполнение
//    остального кода.
// 2. Неблокирующий опрос (millis): Рекомендуемый метод. Программа проверяет
// состояние кнопки только тогда, когда прошло
//    нужное количество миллисекунд.
// 3. Прерывания по таймеру: Опрос кнопки строго каждые 1–2 мс внутри
// обработчика прерывания таймера (например, Timer0)
//    позволяет добиться высочайшей стабильности и освободить основной цикл
//    loop.
///////////////////////////////////////////////////////////////////////////////
// Важно: Слишком частый опрос (быстрее 1–2 мс) избыточен и может привести к
// ложным срабатываниям из-за того, что алгоритм «увидит» каждое колебание
// дребезга как отдельное нажатие. Оптимальная частота опроса кнопки для Arduino
// зависит от задачи и особенностей работы кнопки. Важно учитывать, что сигнал
// кнопки при замыкании и размыкании может иметь дребезг контактов, который
// нужно «погасить» (debounce) для однозначного определения состояния кнопки.
// Также нужно избегать помех, которые могут приводить к ложным нажатиям кнопки,
// например при длинных проводах до кнопки или наличии рядом индуктивной
// нагрузки. Рекомендации
// 1. Периодический опрос. Например, каждые 50 мс (20 раз в секунду) —
// достаточно, чтобы «пропустить» дребезг даже от
//    самой плохой кнопки и снизить вероятность поймать внешние «помехи».
// 2. Учёт времени устойчивого состояния сигнала. Рекомендуется выбирать это
// время в пределах 20–30 мс. Нужно дождаться,
//    когда в течение определённого времени состояние кнопки будет стабильным, и
//    только тогда принимать решение.
// 3. Обработка изменения состояния. Клик — это событие, происходящее при
// изменении одного состояния на другое, а не
//    время удержания кнопки. Именно изменение состояния и нужно обрабатывать,
//    чтобы избежать предсказывания реакции пользователя.
///////////////////////////////////////////////////////////////////////////////
// Учёт режима работы кнопки.
// Например, для «высокоскоростных» комбинаций может хватать периода опроса 10
// мс, но для музыкальных целей такого периода, скорее всего, окажется
// недостаточно. Если логика программы реализована в виде циклического или
// однопроходного алгоритма, опрос состояния кнопки можно производить в конце
// или начале каждого нового цикла программы.
///////////////////////////////////////////////////////////////////////////////
// Код
// Для опроса кнопки с определённой частотой можно использовать прерывание по
// таймеру. Если разместить функцию обработки кнопки в прерывании таймера,
// которое будет выполняться с некоторым постоянным интервалом, то опрос кнопки
// может производиться независимо от состояния фоновой программы. Для работы с
// таймерами рекомендуют специальную библиотеку, например, TimerInterrupt.h.
// Важно: для надёжной работы с кнопками метод, отслеживающий состояние кнопки,
// нельзя вызывать более одного раза на один проход loop() — в противном случае
// можно пропускать события кнопки.
///////////////////////////////////////////////////////////////////////////////
// Ошибки
// 1. Ложные срабатывания. Если не обрабатывать дребезг контактов, на каждое
// нажатие кнопка вырабатывает много фронтов
//    сигналов, и программа инвертирует состояние светодиода на каждый фронт.
// 2. Пропускание событий. Если функция, отслеживающая состояние кнопки,
// запускается слишком редко, она не может
//    гарантированно получить данные о её состоянии.
// 3. Неправильная последовательность сигналов. Например, если частота опроса
// установлена 4 мс, контроллер может видеть
//    зажатые выводы в неправильной последовательности.
// Чтобы избежать ошибок, рекомендуется проводить эксперименты и настраивать
// частоту опроса с учётом особенностей кнопки и задачи.
///////////////////////////////////////////////////////////////////////////////
// Способ подключения кнопки к контроллеру. В зависимости от выбранного
// способа, логический уровень, соответствующий состоянию "кнопка не нажата",
// может быть HIGH или LOW.
// 1. С подтягивающим резистором к питанию (Pull-Up).
//    Когда кнопка не нажата, пин контроллера через резистор 10 кОм подключён
//    к +5V → HIGH. При нажатии кнопка замыкает пин на GND → LOW.
// 2. С подтягивающим резистором к земле (Pull-Down).
//    Когда кнопка не нажата, пин контроллера через резистор 10 кОм подключён
//    к GND → LOW. При нажатии кнопка замыкает пин на +5V → HIGH.

#ifndef BUTTON_H
#define BUTTON_H

#include "callbacks_table.h"
#include "hardware.h"

#include <Arduino.h>
#include <stdint.h>

namespace wokwi
{

  class button : public wokwi::electronic_component<wokwi::channel_connect_params>
  {
  public:
    using pointer_to_callback_function = typename wokwi::table_of_callback_functions<wokwi::button_events>::pointer_to_callback_function;
    using table_of_pointers_to_callback_function = wokwi::table_of_callback_functions<wokwi::button_events>;
    using event = wokwi::button_events;
    using basic = wokwi::electronic_component<wokwi::channel_connect_params>;
    const static auto channel_number = basic::number_of_channels - 1u;

    enum class behaviour : uint8_t
    {
      pull_up,  // Кнопка не нажата - HIGH, кнопка нажата - LOW
      pull_down // Кнопка не нажата - LOW, кнопка нажата - HIGH
    }; // enum class behaviour

    constexpr button() = default;
    explicit button(uint8_t pin_number, behaviour idle_value = behaviour::pull_up);
    button(const button &) = default;
    button(button &&) = default;
    ~button() = default;

    button &operator=(const button &) = default;
    button &operator=(button &&) = default;
    /**
     * @brief
     * @param action
     */
    void pressed(const pointer_to_callback_function action) { _handlers.add_handler(event::pressed, action); }
    void release(const pointer_to_callback_function action) { _handlers.add_handler(event::release, action); }

    void update();
    uint8_t get_signal() const { return _signal_value; }
    uint8_t get_pin_number() const { return basic::get_pin_number(channel_number); }

  private:
    table_of_pointers_to_callback_function _handlers{};
    behaviour _idle_signal_value{};
    /**
     * @brief Переменная _signal_value сохраняет состояние кнопки между вызовами функции update().
     * @brief который зависит от выбранного способа подключения кнопки к контроллеру (pull-up или pull-down).
     * @brief В дальнейшем при каждом вызове функции update() будем сравнивать текущее состояние
     * @brief кнопки с _signal_value, чтобы определить, изменилось ли состояние кнопки (нажата или отпущена)
     * @brief по сравнению с предыдущим состоянием. Если состояние изменилось, то будем обрабатывать
     * @brief это изменение и обновлять _signal_value.
     */
    uint8_t _signal_value{};
  };
  /**
   * @brief
   * @param pin_number
   * @param idle_value
   */
  wokwi::button::button(uint8_t pin_number, behaviour idle_value)
      : wokwi::electronic_component<wokwi::channel_connect_params>{{pin_number, wokwi::signal_type::digital,
                                                                    idle_value == behaviour::pull_up
                                                                        ? wokwi::signal_direction::input_pullup
                                                                        : wokwi::signal_direction::input}},
        _idle_signal_value{idle_value},
        _signal_value{idle_value == behaviour::pull_up ? static_cast<uint8_t>(wokwi::digital_signal_values::high)
                                                       : static_cast<uint8_t>(wokwi::digital_signal_values::low)}
  {
  }
  /**
   * @brief
   */
  void wokwi::button::update()
  {
    const uint8_t curr_signal_value = basic::read(channel_number);
    using signal_value = wokwi::digital_signal_values;
    auto call = [this, curr_signal_value](signal_value pressed, signal_value release)
    {
      if (curr_signal_value != _signal_value)
      {
        if (curr_signal_value == static_cast<uint8_t>(pressed))
          if (_handlers.handler(event::pressed))
            _handlers.handler(event::pressed)();
        if (curr_signal_value == static_cast<uint8_t>(release))
          if (_handlers.handler(event::release))
            _handlers.handler(event::release)();
      }
      _signal_value = curr_signal_value;
    };

    _idle_signal_value == behaviour::pull_up ? call(signal_value::low, signal_value::high) : call(signal_value::high, signal_value::low);
  }
} // namespace wokwi

#endif // !BUTTON_H